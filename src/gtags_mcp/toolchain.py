def _download(url: str, dest: Path, expected_sha256: str | None = None) -> None:
    """Fetch a URL to *dest*, optionally enforcing a SHA256 checksum."""
    request = urllib.request.Request(url, headers={"User-Agent": "gtags-mcp-setup"})
    digest = hashlib.sha256()
    try:
        with urllib.request.urlopen(request, timeout=DOWNLOAD_TIMEOUT_SECONDS) as resp:
            with dest.open("wb") as fh:
                while chunk := resp.read(1 << 16):
                    digest.update(chunk)
                    fh.write(chunk)
    except (urllib.error.URLError, OSError) as exc:
        raise SetupError(f"download failed for {url}: {exc}") from exc
    if expected_sha256 and digest.hexdigest() != expected_sha256:
        dest.unlink(missing_ok=True)
        raise SetupError(
            f"checksum mismatch for {url}: expected {expected_sha256}, "
            f"got {digest.hexdigest()} — refusing to install"
        )


def _extract_tarball(tarball: Path, dest: Path) -> Path:
    """Safely extract a tarball, returning its single top-level directory."""
    with tarfile.open(tarball) as tar:
        members = tar.getmembers()
        for member in members:
            target = (dest / member.name).resolve()
            if not target.is_relative_to(dest.resolve()):
                raise SetupError(f"unsafe path in tarball: {member.name}")
        if sys.version_info >= (3, 12):
            tar.extractall(dest, filter="data")
        else:
            tar.extractall(dest)  # members validated above
    tops = {m.name.split("/", 1)[0] for m in members}
    if len(tops) == 1:
        return dest / tops.pop()
    return dest


def _platform_tag() -> str | None:
    """Platform tag used in this project's prebuilt release assets."""
    machine = platform.machine().lower()
    machine = {"amd64": "x86_64", "arm64": "aarch64"}.get(machine, machine)
    if sys.platform.startswith("linux"):
        return f"linux-{machine}"
    if sys.platform == "darwin":
        return f"macos-{machine}"
    return None


def _host_glibc() -> tuple[int, int] | None:
    """(major, minor) of the host glibc; None on non-Linux/musl/unknown."""
    if not sys.platform.startswith("linux"):
        return None
    version = ""
    try:
        version = os.confstr("CS_GNU_LIBC_VERSION") or ""
    except (ValueError, OSError):
        pass
    if not version:
        lib, ver = platform.libc_ver()
        if lib == "glibc":
            version = f"glibc {ver}"
    match = re.match(r"glibc[ -](\d+)\.(\d+)", version)
    if not match:
        return None
    return int(match.group(1)), int(match.group(2))


