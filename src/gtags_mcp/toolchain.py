# Long placeholder prefix baked into prebuilt binaries by CI; setup patches it
# to the real managed home (conda-style relocation, works while the real path
# is shorter than the placeholder).
PLACEHOLDER_PREFIX = "/opt/gtags-mcp-placeholder-prefix" + "_" * 167


def _patch_embedded_prefix(binary: Path, old_prefix: str, new_prefix: str) -> None:
    """Rewrite *old_prefix* paths inside an installed file.

    Text files (shell/perl helper scripts) get a plain replacement. Compiled
    binaries get conda-style patching: each NUL-terminated string is rewritten
    in place and padded with NULs so offsets never shift — which requires the
    new prefix to be no longer than the placeholder.
    """
    if not binary.is_file() or binary.is_symlink():
        return
    data = binary.read_bytes()
    old = old_prefix.encode()
    if old not in data:
        return
    new = new_prefix.encode()
    if b"\0" not in data:
        # Text script: lengths may change freely.
        binary.write_bytes(data.replace(old, new))
        return
    if len(new) > len(old):
        raise SetupError(
            f"cannot relocate {binary.name}: {new_prefix} is longer than the "
            "placeholder prefix"
        )
    out = bytearray()
    pos = 0
    while (idx := data.find(old, pos)) != -1:
        end = data.find(b"\0", idx)
        if end == -1:
            # Trailing occurrence without a terminator: plain replacement.
            out += data[pos:idx] + new + data[idx + len(old) :]
            pos = len(data)
            break
        tail = data[idx + len(old) : end]
        patched = new + tail
        patched += b"\0" * (end - idx - len(patched))
        out += data[pos:idx] + patched
        pos = end
    out += data[pos:]
    binary.write_bytes(bytes(out))


def _verify_managed_global(log) -> bool:
    """Execute the just-installed managed global/gtags; False on any failure."""
    for name in ("global", "gtags"):
        ok, tail = _try_run([str(managed_bin() / name), "--version"], timeout=15)
        if not ok:
            log(f"  installed {name} does not run on this host: {tail.strip()}")
            return False
    return True


def install_global_prebuilt(log) -> bool:
    """Install Global from this repo's release binaries. False if unavailable."""
    tag_platform = _platform_tag()
    if tag_platform is None:
        return False
    glibc = _host_glibc()
    if glibc is not None and glibc < PREBUILT_MIN_GLIBC:
        log(
            f"  host glibc {glibc[0]}.{glibc[1]} is older than "
            f"{PREBUILT_MIN_GLIBC[0]}.{PREBUILT_MIN_GLIBC[1]} required by the "
            "prebuilt binaries; building from source instead"
        )
        return False
    base = f"https://github.com/{PREBUILT_REPO}/releases/download/{PREBUILT_TAG}"
    asset = f"global-{GLOBAL_VERSION}-{tag_platform}.tar.gz"
    with tempfile.TemporaryDirectory(prefix="gtags-mcp-") as tmp:
        tmpdir = Path(tmp)
        try:
            _download(f"{base}/checksums.txt", tmpdir / "checksums.txt")
        except SetupError:
            log(f"  no prebuilt binaries published for {PREBUILT_TAG}; skipping")
            return False
        checksums = dict(
            reversed(line.split())
            for line in (tmpdir / "checksums.txt").read_text().splitlines()
            if len(line.split()) == 2
        )
        if asset not in checksums:
            log(f"  no prebuilt binary for {tag_platform}; skipping")
            return False
        log(f"  downloading {asset} ...")
        _download(f"{base}/{asset}", tmpdir / asset, expected_sha256=checksums[asset])
        extracted = _extract_tarball(tmpdir / asset, tmpdir / "x")
        home = managed_home()
        home.mkdir(parents=True, exist_ok=True)
        for sub in ("bin", "lib", "share", "libexec"):
            src = extracted / sub
            if src.is_dir():
                shutil.copytree(src, home / sub, dirs_exist_ok=True)
    log("  relocating binaries ...")
    for path in (home / "bin").iterdir():
        _patch_embedded_prefix(path, PLACEHOLDER_PREFIX, str(home))
    libdir = home / "lib" / "gtags"
    if libdir.is_dir():
        for path in libdir.glob("*.so"):
            _patch_embedded_prefix(path, PLACEHOLDER_PREFIX, str(home))
    # Raising (not returning False) matters: files are already extracted into
    # the managed home, and only run_setup's exception path wipes it before
    # falling back to a source build.
    if not _verify_managed_global(log):
        raise SetupError(
            "prebuilt binaries are not runnable on this host (incompatible glibc?)"
        )
    return True


def _require(*names: str) -> None:
    missing = [name for name in names if shutil.which(name) is None]
    if missing:
        raise SetupError(
            f"required build tool(s) not found: {', '.join(missing)}. "
            "Install a C compiler and make, or wait for prebuilt binaries."
        )


def install_global_from_source(log) -> None:
    """Download, verify, and build GNU Global into the managed home."""
    _require("make")
    if shutil.which("cc") is None and shutil.which("gcc") is None:
        raise SetupError(
            "no C compiler found (need cc or gcc). "
            "Install one, or wait for prebuilt binaries."
        )
    home = managed_home()
    home.mkdir(parents=True, exist_ok=True)
    with tempfile.TemporaryDirectory(prefix="gtags-mcp-build-") as tmp:
        tmpdir = Path(tmp)
        tarball = tmpdir / f"global-{GLOBAL_VERSION}.tar.gz"
        log(f"  downloading GNU Global {GLOBAL_VERSION} source ...")
        _download(GLOBAL_SOURCE_URL, tarball, expected_sha256=GLOBAL_SOURCE_SHA256)
        srcdir = _extract_tarball(tarball, tmpdir)
        log("  building (this takes a few minutes) ...")

        def _step(args: list[str]) -> None:
            proc = subprocess.run(
                args,
                cwd=srcdir,
                capture_output=True,
                text=True,
                timeout=BUILD_TIMEOUT_SECONDS,
            )
            if proc.returncode != 0:
                raise SetupError(
                    f"{' '.join(args[:2])} failed:\n{proc.stderr[-2000:]}"
                )

        # --disable-gtagscscope drops the only ncurses dependency;
        # --with-included-ltdl statically links the bundled libltdl so the
        # result does not depend on a system libltdl.so.
        _step(
            [
                "./configure",
                f"--prefix={home}",
                "--disable-gtagscscope",
                "--with-included-ltdl",
            ]
        )
        _step(["make", f"-j{os.cpu_count() or 2}"])
        _step(["make", "install"])


def write_relocated_conf(log) -> None:
    """Generate ``~/.gtags-mcp/gtags.conf`` with corrected absolute paths.

    The shipped gtags.conf refers to plugin parsers via the compiled-in
    ``$libdir`` and has an empty ``ctagscom``; this rewrite makes the managed
    install self-contained and points ctags at whatever we resolved.
    """
    shipped = managed_home() / "share" / "gtags" / "gtags.conf"
    if not shipped.is_file():
        return
    text = shipped.read_text()
    text = text.replace("$libdir", str(managed_home() / "lib"))
    if ctags := find_ctags():
        text = re.sub(r"(:ctagscom=)[^:\\\n]*", rf"\g<1>{ctags}", text)
    managed_conf().write_text(text)
    log(f"  wrote {managed_conf()}")
    _fix_pygments_shebang(log)


def _fix_pygments_shebang(log) -> None:
    """Point the shipped pygments parser script at ``python3``.

    Its shebang says ``#!/usr/bin/env python``, but modern distros ship only
    ``python3`` — with the original shebang the plugin parser dies at startup
    and every gtags run under a pygments label fails with "unexpected EOF".
    """
    script = managed_home() / "share" / "gtags" / "script" / "pygments_parser.py"
    try:
        lines = script.read_text().splitlines(keepends=True)
    except OSError:
        return
    if lines and lines[0].strip() == "#!/usr/bin/env python":
        script.write_text("#!/usr/bin/env python3\n" + "".join(lines[1:]))
        log(f"  fixed python3 shebang in {script}")


