def install_ctags(log) -> bool:
    """Install universal-ctags from its official binary releases.

    An existing ctags only counts when it can actually power the server's
    features: it must be Universal Ctags with JSON output (the metadata
    enrichment requirement). An old Exuberant ctags on PATH is not enough —
    we install our own into the managed bin dir, which wins discovery order.
    """
    from . import enrich  # function-local: enrich imports this module

    if existing := find_ctags():
        usable, detail = enrich.probe_binary(existing)
        if usable:
            log(f"  ctags already available (Universal +json): {existing}")
            return True
        log(
            f"  found {existing} but it cannot emit JSON output ({detail}) — "
            f"installing universal-ctags into {managed_bin()}"
        )
    machine = platform.machine().lower()
    machine = {"amd64": "x86_64", "arm64": "aarch64"}.get(machine, machine)
    if sys.platform.startswith("linux"):
        pattern = rf"uctags-[\d.]+-linux-{re.escape(machine)}\.release\.tar\.gz$"
    elif sys.platform == "darwin":
        mac_machine = "arm64" if machine == "aarch64" else machine
        pattern = rf"uctags-[\d.]+-macos-[\d.]+-{re.escape(mac_machine)}\.release\.tar\.gz$"
    else:
        log("  no prebuilt ctags for this platform; skipping")
        return False
    request = urllib.request.Request(
        CTAGS_NIGHTLY_API, headers={"User-Agent": "gtags-mcp-setup"}
    )
    try:
        with urllib.request.urlopen(request, timeout=DOWNLOAD_TIMEOUT_SECONDS) as resp:
            release = json.load(resp)
    except (urllib.error.URLError, OSError, json.JSONDecodeError) as exc:
        log(f"  could not query ctags releases ({exc}); skipping")
        return False
    assets = [
        asset
        for asset in release.get("assets", [])
        if re.search(pattern, asset.get("name", ""))
    ]
    if not assets:
        log("  no matching ctags release asset; skipping")
        return False
    asset = sorted(assets, key=lambda a: a["name"])[-1]
    log(f"  downloading {asset['name']} ...")
    with tempfile.TemporaryDirectory(prefix="gtags-mcp-ctags-") as tmp:
        tmpdir = Path(tmp)
        tarball = tmpdir / asset["name"]
        _download(asset["browser_download_url"], tarball)
        extracted = _extract_tarball(tarball, tmpdir / "x")
        candidates = list(extracted.rglob("bin/ctags")) or list(extracted.rglob("ctags"))
        binaries = [c for c in candidates if c.is_file() and os.access(c, os.X_OK)]
        if not binaries:
            log("  ctags binary not found inside release archive; skipping")
            return False
        managed_bin().mkdir(parents=True, exist_ok=True)
        target = managed_bin() / "ctags"
        shutil.copy2(binaries[0], target)
        target.chmod(0o755)
    enrich.reset_cache()  # the managed path may carry a stale probe result
    usable, detail = enrich.probe_binary(str(target))
    if not usable:
        target.unlink(missing_ok=True)
        log(
            f"  downloaded ctags does not work on this host ({detail}); "
            "removed it — metadata enrichment will be unavailable"
        )
        return False
    log(f"  installed {target}")
    return True


def _try_run(args: list[str], timeout: int = 300) -> tuple[bool, str]:
    """Run a bootstrap command; returns (ok, output tail). Never raises."""
    try:
        proc = subprocess.run(args, capture_output=True, text=True, timeout=timeout)
    except (OSError, subprocess.TimeoutExpired) as exc:
        return False, str(exc)
    return proc.returncode == 0, ((proc.stderr or "") + (proc.stdout or ""))[-500:]


def _install_pygments_venv(log) -> bool:
    """Self-contained strategy: uv-managed venv + python3 shim in managed bin.

    Works even when the system python has no pip/venv (common on minimal
    Ubuntu/Debian images). The shim wins at runtime because the server
    prepends the managed bin dir to PATH for every spawned process.
    """
    uv = shutil.which("uv") or str(Path.home() / ".local" / "bin" / "uv")
    if not Path(uv).is_file():
        return False
    venv = managed_home() / "pyenv"
    ok, err = _try_run([uv, "venv", "--quiet", str(venv)])
    if not ok:
        log(f"  uv venv failed: {err.strip()}")
        return False
    ok, err = _try_run(
        [uv, "pip", "install", "--quiet", "--python", str(venv / "bin" / "python"), "pygments"]
    )
    if not ok:
        log(f"  uv pip install failed: {err.strip()}")
        return False
    if not _imports_pygments(str(venv / "bin" / "python3")):
        return False
    managed_bin().mkdir(parents=True, exist_ok=True)
    shim = managed_python()
    shim.unlink(missing_ok=True)
    shim.symlink_to(venv / "bin" / "python3")
    log(f"  installed Pygments in {venv} (python3 shim: {shim})")
    return True


def install_pygments(log) -> bool:
    """Ensure Pygments is importable by the plugin parser's python3.

    Tries, in order:
    1. already available (managed shim or system python),
    2. a uv-managed venv inside the managed home (no system pip needed),
    3. ``python3 -m pip install --user`` (with a PEP 668
       ``--break-system-packages`` retry),
    4. ``python3 -m ensurepip`` to bootstrap pip, then (3) again.
    """
    if pygments_available():
        log("  Pygments already available; skipping")
        return True

    log("  installing Pygments (self-contained venv) ...")
    if _install_pygments_venv(log):
        return True

    python3 = shutil.which("python3") or sys.executable
    log("  installing Pygments (pip --user) ...")
    pip_user = [python3, "-m", "pip", "install", "--user", "--quiet", "pygments"]
    ok, err = _try_run(pip_user)
    if not ok and "externally-managed" in err:
        ok, err = _try_run(pip_user + ["--break-system-packages"])
    if not ok and "No module named pip" in err:
        boot_ok, _ = _try_run([python3, "-m", "ensurepip", "--user"])
        if boot_ok:
            ok, err = _try_run(pip_user)
            if not ok and "externally-managed" in err:
                ok, err = _try_run(pip_user + ["--break-system-packages"])
    if not ok:
        log(
            "  could not install Pygments; multi-language references disabled.\n"
            "  Fix manually with one of:\n"
            "    uv venv ~/.gtags-mcp/pyenv && uv pip install --python ~/.gtags-mcp/pyenv/bin/python pygments\n"
            "    sudo apt install python3-pygments\n"
            f"  (last error: {err.strip()})"
        )
        return False
    return pygments_available()


