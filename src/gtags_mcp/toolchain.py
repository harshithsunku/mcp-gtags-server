# --------------------------------------------------------------------------
# Pinned versions and download sources
# --------------------------------------------------------------------------

GLOBAL_VERSION = "6.6.15"
GLOBAL_SOURCE_URL = f"https://ftp.gnu.org/gnu/global/global-{GLOBAL_VERSION}.tar.gz"
GLOBAL_SOURCE_SHA256 = "cf0937cb3ed521b2ab1acfa7aff45103040b860bb642c4c2f094ac3a3fe86024"

# Prebuilt Global binaries published on this project's GitHub releases under
# the tag ``global-v<version>`` (built by release-binaries.yml); setup falls
# back to a source build when no asset matches the platform.
PREBUILT_REPO = "harshithsunku/mcp-gtags-server"
PREBUILT_TAG = f"global-v{GLOBAL_VERSION}"

# Prebuilt Linux binaries are built in manylinux_2_28 containers and need at
# least this glibc; older hosts skip the download and build from source.
PREBUILT_MIN_GLIBC = (2, 28)

# Official universal-ctags binary releases (built by the ctags project).
CTAGS_NIGHTLY_API = (
    "https://api.github.com/repos/universal-ctags/ctags-nightly-build/releases/latest"
)

DOWNLOAD_TIMEOUT_SECONDS = 120
BUILD_TIMEOUT_SECONDS = 1800

# Universal flavours first: enrichment and export recovery require Universal
# Ctags (+json), which Exuberant ctags can never provide. Distros name the
# universal binary "ctags-universal" (Debian/Ubuntu) or "universal-ctags";
# the generic "ctags" may be either flavour, and exuberant is the last
# resort (still fine as the gtags pygments-plugin backend).
_CTAGS_NAMES = ("universal-ctags", "ctags-universal", "ctags", "ctags-exuberant")


class SetupError(RuntimeError):
    """A toolchain bootstrap step failed."""


def managed_home() -> Path:
    """Root of the user-space install (``~/.gtags-mcp`` by default)."""
    return Path(os.environ.get("GTAGS_MCP_HOME", "~/.gtags-mcp")).expanduser()


def managed_bin() -> Path:
    return managed_home() / "bin"


def managed_conf() -> Path:
    """Relocated gtags.conf generated by setup (used via GTAGSCONF)."""
    return managed_home() / "gtags.conf"


# --------------------------------------------------------------------------
# Binary discovery
# --------------------------------------------------------------------------


def _search_dirs(bin_dir: str | None, project_root: Path | None) -> list[Path]:
    """User-space directories to search before falling back to PATH."""
    dirs: list[Path] = []
    explicit = (
        bin_dir
        or os.environ.get("GTAGS_MCP_BIN_DIR")
        or config.get_setting("bin_dir", project_root)
    )
    if explicit:
        dirs.append(Path(explicit).expanduser())
    dirs.append(managed_bin())
    dirs.append(Path.home() / ".local" / "bin")
    return dirs


def find_tool(
    name: str, bin_dir: str | None = None, project_root: Path | None = None
) -> str | None:
    """Resolve a binary: explicit dir > managed dir > PATH > ~/.local/bin."""
    candidates = _search_dirs(bin_dir, project_root)
    # PATH slots in after the managed dir (index 0 or 1 is explicit/managed).
    for directory in candidates[:-1]:
        exe = directory / name
        if exe.is_file() and os.access(exe, os.X_OK):
            return str(exe)
    if found := shutil.which(name):
        return found
    exe = candidates[-1] / name
    if exe.is_file() and os.access(exe, os.X_OK):
        return str(exe)
    return None


def find_global(bin_dir: str | None = None, project_root: Path | None = None) -> str | None:
    return find_tool("global", bin_dir, project_root)


def find_gtags(bin_dir: str | None = None, project_root: Path | None = None) -> str | None:
    return find_tool("gtags", bin_dir, project_root)


def find_ctags(bin_dir: str | None = None, project_root: Path | None = None) -> str | None:
    """Resolve any ctags flavour, honouring location priority over name.

    A ctags in the explicit/managed user-space directories always beats one
    on PATH, regardless of which of the known binary names it uses.
    """
    dirs = _search_dirs(bin_dir, project_root)
    for directory in dirs[:-1]:
        for name in _CTAGS_NAMES:
            exe = directory / name
            if exe.is_file() and os.access(exe, os.X_OK):
                return str(exe)
    for name in _CTAGS_NAMES:
        if found := shutil.which(name):
            return found
    for name in _CTAGS_NAMES:
        exe = dirs[-1] / name
        if exe.is_file() and os.access(exe, os.X_OK):
            return str(exe)
    return None


def runtime_env(global_path: str | None) -> dict[str, str]:
    """Extra environment for running a managed (relocated) Global install."""
    env: dict[str, str] = {}
    if (
        global_path
        and "GTAGSCONF" not in os.environ
        and Path(global_path).is_relative_to(managed_home())
        and managed_conf().is_file()
    ):
        env["GTAGSCONF"] = str(managed_conf())
    return env


def managed_python() -> Path:
    """python3 shim inside the managed bin dir (created by install_pygments)."""
    return managed_bin() / "python3"


def _imports_pygments(python3: str) -> bool:
    try:
        probe = subprocess.run(
            [python3, "-c", "import pygments"], capture_output=True, timeout=15
        )
    except (OSError, subprocess.TimeoutExpired):
        return False
    return probe.returncode == 0


def pygments_available() -> bool:
    """True when the python3 the plugin parser will use can import Pygments.

    The parser script resolves ``python3`` via PATH; the server prepends the
    managed bin dir, so a managed python3 shim takes priority when present.
    """
    if managed_python().exists():
        return _imports_pygments(str(managed_python()))
    python3 = shutil.which("python3") or sys.executable
    return _imports_pygments(python3)


