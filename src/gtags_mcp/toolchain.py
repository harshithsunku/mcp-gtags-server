def _managed_global_version() -> str | None:
    """Version of the Global install inside the managed home, if any."""
    exe = managed_bin() / "global"
    if not exe.is_file():
        return None
    try:
        proc = subprocess.run(
            [str(exe), "--version"], capture_output=True, text=True, timeout=15
        )
        first = proc.stdout.splitlines()[0]
        return first.strip().rsplit(" ", 1)[-1]
    except (OSError, subprocess.TimeoutExpired, IndexError):
        return None


def run_setup(with_ctags: bool = True, force: bool = False, log=print) -> int:
    """Install the full toolchain into user space. Returns an exit code.

    Idempotent and self-updating: when the managed install's Global version
    differs from the pinned ``GLOBAL_VERSION`` (e.g. after the package was
    upgraded), the whole managed home is removed and reinstalled fresh.
    """
    log(f"mcp-gtags-server setup — installing into {managed_home()} (no sudo needed)")

    managed_version = _managed_global_version()
    if managed_version and managed_version != GLOBAL_VERSION:
        log(
            f"* Managed GNU Global {managed_version} is outdated "
            f"(this release pins {GLOBAL_VERSION}) — removing {managed_home()} "
            "and reinstalling ..."
        )
        shutil.rmtree(managed_home(), ignore_errors=True)
    elif (managed_bin() / "global").is_file() and managed_version is None:
        log(
            f"* Managed GNU Global in {managed_bin()} exists but cannot run "
            f"(incompatible binaries?) — removing {managed_home()} "
            "and reinstalling ..."
        )
        shutil.rmtree(managed_home(), ignore_errors=True)

    existing = find_global()
    if existing and not force and not Path(existing).is_relative_to(managed_home()):
        log(f"* GNU Global already available: {existing}")
    elif (managed_bin() / "global").is_file() and not force:
        log(f"* GNU Global already installed in {managed_bin()}")
    else:
        log("* Installing GNU Global ...")
        try:
            prebuilt_ok = install_global_prebuilt(log)
        except Exception as exc:  # noqa: BLE001 — never let prebuilt kill setup
            log(f"  prebuilt install failed ({exc}); falling back to source build")
            shutil.rmtree(managed_home(), ignore_errors=True)
            prebuilt_ok = False
        if not prebuilt_ok:
            try:
                install_global_from_source(log)
            except SetupError as exc:
                log(f"error: {exc}")
                return 1
        log(f"  installed GNU Global {GLOBAL_VERSION}")

    if with_ctags:
        log("* Installing universal-ctags (multi-language definitions) ...")
        try:
            install_ctags(log)
        except SetupError as exc:
            log(f"  warning: {exc}")
        log("* Installing Pygments (multi-language references) ...")
        install_pygments(log)

    write_relocated_conf(log)

    global_path = find_global()
    if not global_path:
        log("error: setup finished but `global` is still not resolvable")
        return 1
    log("\nDone. Toolchain:")
    log(doctor_report())
    return 0


def doctor_report(project_root: Path | None = None) -> str:
    """Human-readable status of every toolchain component and config file."""

    def version_of(exe: str | None) -> str:
        if not exe:
            return "NOT FOUND"
        try:
            proc = subprocess.run(
                [exe, "--version"], capture_output=True, text=True, timeout=15
            )
            first = (proc.stdout or proc.stderr).splitlines()[0].strip()
            if "GLIBC_" in first:
                first += " — run 'mcp-gtags-server setup --force' to rebuild from source"
            return f"{exe}  ({first})"
        except (OSError, subprocess.TimeoutExpired, IndexError):
            return f"{exe}  (version check failed)"

    lines = [
        f"  managed home : {managed_home()}"
        + ("" if managed_home().is_dir() else "  (not created yet)"),
        f"  global       : {version_of(find_global(project_root=project_root))}",
        f"  gtags        : {version_of(find_gtags(project_root=project_root))}",
        f"  ctags        : {version_of(find_ctags(project_root=project_root))}",
        f"  pygments     : {'available' if pygments_available() else 'NOT FOUND'}",
        f"  gtags.conf   : {managed_conf() if managed_conf().is_file() else '(none generated)'}",
        f"  user config  : {config.user_config_path()}"
        + ("" if config.user_config_path().is_file() else "  (not present)"),
    ]
    if project_root:
        project_file = project_root / config.PROJECT_CONFIG_NAME
        lines.append(
            f"  project config: {project_file}"
            + ("" if project_file.is_file() else "  (not present)")
        )
    if not find_global(project_root=project_root):
        lines.append(
            "\n  GNU Global is missing — run `mcp-gtags-server setup` to install it "
            "into user space (no sudo needed)."
        )
    elif not pygments_available():
        lines.append(
            "\n  Pygments is missing — multi-language references are disabled. "
            "Re-run `mcp-gtags-server setup` to install it into user space."
        )
    return "\n".join(lines)
