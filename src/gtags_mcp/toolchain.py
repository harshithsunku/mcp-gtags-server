"""User-space toolchain management for GNU Global, ctags and Pygments.

The server never assumes ``gtags``/``global`` come from a system package.
Binaries are resolved through a search order that puts user-space locations
first, and ``mcp-gtags-server setup`` can install the whole toolchain without root:

1. an explicit ``bin_dir`` (tool argument, ``--bin-dir``, ``GTAGS_MCP_BIN_DIR``
   env var, or ``bin_dir`` in a config file),
2. the managed home ``~/.gtags-mcp/bin`` (override with ``GTAGS_MCP_HOME``),
3. whatever is on ``PATH``,
4. ``~/.local/bin``.

``setup`` install strategies for GNU Global, in order:

- **Prebuilt binaries** from this project's GitHub releases (checksum
  verified, relocated with conda-style path patching). Skipped silently when
  no release asset exists for the platform.
- **Build from source**: the pinned GNU Global tarball (SHA256 verified) is
  compiled with ``--prefix=~/.gtags-mcp`` — needs only ``cc`` and ``make``.

``setup`` also (optionally) installs universal-ctags from its official
nightly-build releases and Pygments via ``python3 -m pip install --user`` to
unlock multi-language indexing.
"""

